export function getStateOfCountry(params: {
  countryCode: string
  states?: States
}): StateOption[] {
  const { countryCode, states: customStates } = params

  if (!countryCode) {
    return []
  }

  // Use custom states if provided, otherwise use default
  const statesData = customStates || STATE_DATA
  return statesData[countryCode] || []
}

export function isValidState(params: {
  stateCode: string
  countryCode: string
  states?: States
}): boolean {
  const { stateCode, countryCode, states } = params

  if (!stateCode || !countryCode) {
    return false
  }

  const availableStates = getStateOfCountry({ countryCode, states })
  return availableStates.some((state) => state.value === stateCode)
}

export function requiresStateSelection(countryCode: string): boolean {
  return COUNTRIES_WITH_STATES.has(countryCode)
}
